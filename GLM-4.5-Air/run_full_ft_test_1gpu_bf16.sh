    fi

    if [[ "${overall_exit}" -eq 0 ]]; then
        ok "Completed — log directory: ${LOG_DIR}"
    else
        warn "Some phases failed — check: ${LOG_DIR}/SUMMARY.md"
    fi
    return "${overall_exit}"
}

